"""
TC_M_28 - Certificate Update EV - Success
Use case: M02 | Requirements: M02.FR.01
M02.FR.01: Upon receiving a CertificateUpdateReq the Charging Station SHALL forward the request to the CSMS using the Get15118EVCertificateRequest message with action = Update. The CSMS is responsible for forwarding it to the secondary actor which will process the CertificateUpdateRequest. This could be a contract certificate pool as
System under test: CSMS

Description:
    The EV initiates updating the existing certificate. The Charging Station forwards the update
    request to the CSMS.

Purpose:
    To verify if the CSMS is able to return the Raw CertificateInstallationRes response for the
    EV to the Charging Station.

Main:
    1. The OCTT sends a Get15118EVCertificateRequest with action = Update
    2. The CSMS responds with a Get15118EVCertificateResponse

Tool validations:
    * Step 2: Get15118EVCertificateResponse
      - status = Accepted
      - exiResponse = <Raw CertificateInstallationRes response for the EV, Base64 encoded>

Configuration:
    CSMS_ADDRESS              - WebSocket URL of the CSMS
    BASIC_AUTH_CP             - Charge Point identifier
    BASIC_AUTH_CP_PASSWORD    - Charge Point password
    CONFIGURED_CONNECTOR_ID   - Connector id (default 1)
"""
import asyncio
import logging
import os
import sys
import time

import pytest
import websockets

sys.path.insert(0, os.path.dirname(os.path.dirname(os.path.abspath(__file__))))

from ocpp.v201.enums import (
    RegistrationStatusEnumType,
    ConnectorStatusEnumType,
    CertificateActionEnumType,
    Iso15118EVCertificateStatusEnumType,
)

from tzi_charge_point import TziChargePoint
from utils import get_basic_auth_headers

logging.basicConfig(level=logging.INFO)

CSMS_ADDRESS = os.environ['CSMS_ADDRESS']
BASIC_AUTH_CP = os.environ['BASIC_AUTH_CP']
BASIC_AUTH_CP_PASSWORD = os.environ['BASIC_AUTH_CP_PASSWORD']
CONNECTOR_ID = int(os.environ['CONFIGURED_CONNECTOR_ID'])


@pytest.mark.asyncio
async def test_tc_m_28():
    """Certificate Update EV - Success."""
    cp_id = BASIC_AUTH_CP
    uri = f'{CSMS_ADDRESS}/{cp_id}'
    headers = get_basic_auth_headers(cp_id, BASIC_AUTH_CP_PASSWORD)

    ws = await websockets.connect(
        uri=uri,
        subprotocols=['ocpp2.0.1'],
        extra_headers=headers,
    )
    time.sleep(0.5)

    cp = TziChargePoint(cp_id, ws)
    start_task = asyncio.create_task(cp.start())

    # Boot and establish session
    boot_response = await cp.send_boot_notification()
    assert boot_response.status == RegistrationStatusEnumType.accepted

    await cp.send_status_notification(CONNECTOR_ID, ConnectorStatusEnumType.available)

    # Step 1: CS sends Get15118EVCertificateRequest with action = Update
    response = await cp.send_get_15118_ev_certificate_request(
        iso15118_schema_version='urn:iso:15118:2:2013:MsgDef',
        action=CertificateActionEnumType.update,
        exi_request='dGVzdEVYSVJlcXVlc3REYXRh',  # Base64 encoded test data
    )

    # Tool validation Step 2: status must be Accepted
    assert response.status == Iso15118EVCertificateStatusEnumType.accepted, \
        f"Expected Get15118EVCertificateResponse status=Accepted, got {response.status}"

    # Tool validation Step 2: exiResponse must be present
    assert response.exi_response is not None and len(response.exi_response) > 0, \
        "exiResponse must be present in Get15118EVCertificateResponse"

    logging.info("TC_M_28 completed successfully")
    start_task.cancel()
    await ws.close()

"""
Test case name      Reset EVSE - With Ongoing Transaction - Immediate
Test case Id        TC_B_27_CSMS
Use case Id(s)      B12
Requirement(s)      N/a
System under test   CSMS

Description         This test case covers how the CSMS can remotely request the Charging Station to reset an EVSE
                    by sending a ResetRequest during a transaction. When ResetRequest "Immediate" is sent the
                    charging stations will try to stop all transactions before rebooting.
Purpose             To verify if the CSMS is able to perform the reset mechanism as described at the OCPP specification.

Prerequisite(s)     n/a

Before (Reusable State): EnergyTransferStarted

Test Scenario
Manual Action: Request the CSMS to reboot the charging EVSE with status Immediate
1. The CSMS sends a ResetRequest with status Immediate and evseID <Configured evseId>
2. The OCTT responds with a ResetResponse with status Accepted
3. The OCTT sends a TransactionEventRequest (Ended, ResetCommand, ImmediateReset)
4. The CSMS responds with a TransactionEventResponse

Tool validations
* Step 1:
    Message: ResetRequest
    - type Immediate
    - evseId <Configured evseId>

Post scenario validations:
    N/a
"""

import asyncio
import pytest
import os
import time
import logging

import websockets
from ocpp.v201.enums import (
    RegistrationStatusEnumType, ConnectorStatusEnumType, ResetStatusEnumType
)
from ocpp.v201.call import TransactionEvent

from tzi_charge_point import TziChargePoint
from utils import get_basic_auth_headers, generate_transaction_id, now_iso

logging.basicConfig(level=logging.INFO)

CSMS_ADDRESS = os.environ.get('CSMS_ADDRESS')
BASIC_AUTH_CP = os.environ.get('BASIC_AUTH_CP')
BASIC_AUTH_CP_PASSWORD = os.environ.get('BASIC_AUTH_CP_PASSWORD')
CSMS_ACTION_TIMEOUT = int(os.environ.get('CSMS_ACTION_TIMEOUT', '30'))
CONFIGURED_EVSE_ID = int(os.environ.get('CONFIGURED_EVSE_ID', '1'))
CONFIGURED_CONNECTOR_ID = int(os.environ.get('CONFIGURED_CONNECTOR_ID', '1'))
VALID_ID_TOKEN = os.environ.get('VALID_ID_TOKEN', '100000C01')
VALID_ID_TOKEN_TYPE = os.environ.get('VALID_ID_TOKEN_TYPE', 'Central')


@pytest.mark.asyncio
async def test_tc_b_27():
    """Reset EVSE - With Ongoing Transaction - Immediate: immediate EVSE reset stops transaction."""
    cp_id = BASIC_AUTH_CP
    uri = f'{CSMS_ADDRESS}/{cp_id}'
    headers = get_basic_auth_headers(cp_id, BASIC_AUTH_CP_PASSWORD)

    ws = await websockets.connect(
        uri=uri,
        subprotocols=['ocpp2.0.1'],
        extra_headers=headers,
    )
    time.sleep(0.5)

    cp = TziChargePoint(cp_id, ws)
    cp._reset_response_status = ResetStatusEnumType.accepted
    start_task = asyncio.create_task(cp.start())

    # Boot and establish session
    boot_response = await cp.send_boot_notification()
    assert boot_response.status == RegistrationStatusEnumType.accepted

    await cp.send_status_notification(1, ConnectorStatusEnumType.available)

    # Simulate EnergyTransferStarted - start a transaction
    transaction_id = generate_transaction_id()

    started_event = TransactionEvent(
        event_type='Started',
        timestamp=now_iso(),
        trigger_reason='CablePluggedIn',
        seq_no=0,
        transaction_info={
            'transaction_id': transaction_id,
            'charging_state': 'EVConnected',
        },
        evse={'id': CONFIGURED_EVSE_ID, 'connector_id': CONFIGURED_CONNECTOR_ID},
        id_token={'id_token': VALID_ID_TOKEN, 'type': VALID_ID_TOKEN_TYPE},
    )
    await cp.send_transaction_event_request(started_event)

    # Step 1-2: Wait for CSMS to send ResetRequest with evseId and Immediate
    await asyncio.wait_for(
        cp._received_reset.wait(),
        timeout=CSMS_ACTION_TIMEOUT,
    )

    assert cp._reset_data is not None
    assert cp._reset_data['type'] == 'Immediate', \
        f"Expected Immediate reset type, got: {cp._reset_data['type']}"
    assert cp._reset_data['evse_id'] == CONFIGURED_EVSE_ID, \
        f"Expected evseId {CONFIGURED_EVSE_ID}, got: {cp._reset_data['evse_id']}"

    # Step 3-4: Send TransactionEventRequest (Ended, ResetCommand, ImmediateReset)
    ended_event = TransactionEvent(
        event_type='Ended',
        timestamp=now_iso(),
        trigger_reason='ResetCommand',
        seq_no=1,
        transaction_info={
            'transaction_id': transaction_id,
            'charging_state': 'EVConnected',
            'stopped_reason': 'ImmediateReset',
        },
    )
    await cp.send_transaction_event_request(ended_event)

    start_task.cancel()
    await ws.close()
